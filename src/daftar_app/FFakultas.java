    private void showData(String cari){
        if(cari == null){
            sql = "select id_fakultas as ID, fakultas as FAKULTAS from fakultas";
        } else {
            sql = "select id_fakultas as ID, fakultas as FAKULTAS from fakultas where fakultas like '%"+ cari +"%'";
        }
        
        try {
            ResultSet rs = DB.read(sql);
//        tampilkan
            tbl.setModel(DbUtils.resultSetToTableModel(rs));
            DB.closeDB();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    }
