    private void bt_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_simpanActionPerformed
//        simpan dan update data
        if(st_edit){ // update
            sql = "update fakultas set fakultas = '"+ tf_fakultas.getText().trim()+"' where id_fakultas = "+ id;
        }else { // save
            sql = "insert into fakultas (fakultas) values ('"+ tf_fakultas.getText().trim() + "')";
        }
//        execute sql
        boolean berhasil= false;
       
        try {
            berhasil = DB.exec(sql);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

        if (berhasil){
            kosong();
            showData(null);
            st_edit = false;
            id = 0;
        } else{
            JOptionPane.showMessageDialog(null, "gagal eksekusi data");
        }
        
    }//GEN-LAST:event_bt_simpanActionPerformed

    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        st_edit = true;
        int baris = tbl.getSelectedRow();
        id = Integer.parseInt(tbl.getModel().getValueAt(baris, 0).toString());
        tf_fakultas.setText(tbl.getModel().getValueAt(baris, 1).toString());
    }//GEN-LAST:event_tblMouseClicked

    private void bt_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_hapusActionPerformed
//        confirm
        int respon = JOptionPane.showConfirmDialog(null, "Sure to Delete "+ tf_fakultas.getText().trim()+ "?",
                "Konfirmasi",
                JOptionPane.YES_NO_OPTION);
//        Delete
        if(respon == JOptionPane.YES_OPTION){
            sql = "delete from fakultas where id_fakultas = "+ id;
            boolean berhasil = false;
            
            try {
                berhasil = DB.exec(sql);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
           
            
            if(berhasil){
                kosong();
                showData(null);
                st_edit = false;
            } else {
                JOptionPane.showMessageDialog(null, "Data tidak dihapus");
            }
            
        } else {
            
        }
    }//GEN-LAST:event_bt_hapusActionPerformed

